package sample;

public class Variable {

    public static void main(String[] args) {
        var number1 = 1; 
        System.out.println(number1); 
       
        String str1; 
        str1 = "abc"; 
        System.out.println(str1);
        
        var x = 86400;
        System.out.println(x);

        int a = 123;
        String s = Integer.toString(a);
        System.out.println(s);
    }

}
